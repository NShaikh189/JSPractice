
let browser = '   CHrome';

switch(browser.toLowerCase().trim())
{
    case 'chrome': console.log('chrome browser');
                    break;

    case 'firefox': console.log('firefox browser');
                    break;

    case 'edge': console.log('edge browser');
                    break;

    case 'safari': console.log('safari browser');
                    break;

    default: console.log('Invalid browser.....please enter the correct browser...');
                    break;


}